package Entities;

/**
 *
 * @author farah
 */
public class produits {
    
    private int id;
    private String nom;
    private float prix;
    private int quantite;
    private String image;

    public produits() {
    }

    public produits(String nom, float prix, int quantite, String image) {
        this.nom = nom;
        this.prix = prix;
        this.quantite = quantite;
        this.image = image;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public float getPrix() {
        return prix;
    }

    public void setPrix(float prix) {
        this.prix = prix;
    }

    public int getQuantite() {
        return quantite;
    }

    public void setQuantite(int quantite) {
        this.quantite = quantite;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
    
    
    
    
    
    
}
